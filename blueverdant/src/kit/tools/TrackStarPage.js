function flowyear(curyear){
  if(curyear>11 )
  {
    var stryear = curyear.toString()
    var str = stryear.split('');
    var total = 0
    for(i=str.length-1;i>-1;i--)
    {
      total = total + parseInt(str[i])
    }
    return total
  }
  return curyear
}
function getflowyear(curyear)
{
  if(curyear>11 )
  {
    var x = 11-flowyear(flowyear(curyear))
    return 0==x?9:x
  }
  return curyear
}

